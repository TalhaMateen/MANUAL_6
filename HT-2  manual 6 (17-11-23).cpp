// program for floyd's triangle 
#include <iostream>
using namespace std;
int main() {
// __Taking 5 rows in outer loop __
 for (int i=1;i<=5;i++){
  cout<<endl;  
    // __inner loop for columns__
    for(int j=1;j<=i;j++)
    {
    cout<<j<<"\t";   // __declaring space in integers __
    
    } 
 cout<<endl;        // __declaring new line__s
     
 }
 return 0;
 }
